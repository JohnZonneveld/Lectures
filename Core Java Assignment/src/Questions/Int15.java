package Questions;

public interface Int15 {
	// + - * /
	static double addition(double a, double b) {
		return a + b;
	}
	
	static double substraction(double a, double b) {
		return a - b;
	}
	
	static double multiplication(double a, double b) {
		return a * b;
	}
	
	static double division(double a, double b) {
		return a / b;
	}

}
